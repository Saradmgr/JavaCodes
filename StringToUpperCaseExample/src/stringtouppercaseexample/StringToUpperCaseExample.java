package stringtouppercaseexample;

public class StringToUpperCaseExample {

    public static void main(String[] args) {
        // Initializing string to convert it into UPPERCASE.
        String s1 = "how are you mr. sarad";
        String s2 = s1.toUpperCase();
        System.out.println("After using to.UpperCase 'how are you mr. sarad' changes to : "+s2);
        System.out.println("\n"+"I'm fine, Thank You so much for asking...");
    }
    
}
