package stringlengthexample;

public class StringLengthExample {

    public static void main(String[] args) {
        //length() method in string is used to calculate the number of string. 
        String s1 ="hello";  
        String s2 = "Saraswati Multiple Campus";   // NOTE: The spaces are also considered as a character length. 
        System.out.println("The length of first string is: "+s1.length());   // Here, length() gives length of the string.
        System.out.println("The length of second string is: "+s2.length());  // Here, length() gives length of the string.
    } 
}
